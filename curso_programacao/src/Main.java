import java.util.Locale;

public class Main {

	public static void main(String[] args) {
		System.out.print("Olá Mundo! ");
		System.out.println("Bom Dia ");
		int y=32;
		String nome = "Maria";
		int idade = 31;
		double renda = 4000.0;
		double x = 10.35784;
		
		System.out.println("a variável Y contém " + y  + " e variável x " + x);// símbolo de + neste caso para concatenação
		System.out.printf("%.2f%n", x);// %n tem o mesmo efeito de \n de quebra de linha, 
		System.out.printf("%.4f%n", x);// o numero neste caso o 4 dita quantas casa decimais irá imprimir
		Locale.setDefault(Locale.US);//comando de localização do app, aqui irá tirar a vígula e por o ponto, padrão US
		System.out.printf("%.4f%n", x);
		System.out.printf("RESULTADO = %.2f metros%n", x);
		//%f = ponto flutuante,	%d = inteiro, %s = texto, %n = quebra de linha
		System.out.printf("%s tem %d anos e ganha R$ %.2f reais%n", nome, idade, renda);
		
	}

}
